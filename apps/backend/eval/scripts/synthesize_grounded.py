"""Generate 25 grounded-prompt rows using DeepEval's Synthesizer.

Methodology borrowed from RAGEval (arXiv:2408.01262) — schema → config →
doc → Q/A loop — but executed locally against OptiTrade widget snapshots
extracted from a live PortfolioSnapshotResponse JSON.

Inputs:
  - A representative PortfolioSnapshotResponse (from the running app or
    `apps/backend/data/paper_portfolios.json`).
  - One or more MomentumSnapshot + TechnicalSnapshot JSONs.

Outputs:
  - 25 rows appended to `apps/backend/eval/datasets/internal/grounded_prompts.jsonl`.

Run:
    uv add deepeval
    export OPENAI_API_KEY=...                 # or any model API key
    uv run python apps/backend/eval/scripts/synthesize_grounded.py

Until this script runs, `grounded_prompts.jsonl` contains the 25 hand-
curated rows from `generate_grounded.py`. The two halves should sum to
50 rows, matching §4.3 of `docs/qa-evaluation-plan.md`.
"""

from __future__ import annotations

import json
from pathlib import Path

OUT = Path(__file__).resolve().parents[2] / "eval" / "datasets" / "internal" / "grounded_prompts.jsonl"


def main() -> None:
    try:
        from deepeval.synthesizer import Synthesizer
    except ImportError as exc:
        raise SystemExit(
            "deepeval not installed.\n"
            "Run: uv add deepeval"
        ) from exc

    # 1. Load representative widget snapshots from the running app's data.
    #    Place at least one Portfolio + one Chart + one Pattern JSON in
    #    `eval/snapshots/` so the synthesizer has substrate.
    snapshot_dir = Path(__file__).resolve().parents[2] / "eval" / "snapshots"
    snapshot_paths = list(snapshot_dir.glob("*.json"))
    if not snapshot_paths:
        raise SystemExit(
            f"No snapshot files in {snapshot_dir}. Drop a representative "
            "PortfolioSnapshotResponse + MomentumSnapshot + "
            "ChartPatternDetection JSON there first."
        )

    # 2. Generate 25 Q/A goldens.
    synth = Synthesizer()
    goldens = synth.generate_goldens_from_docs(
        document_paths=[str(p) for p in snapshot_paths],
        chunk_size=512,
        chunk_overlap=64,
        max_goldens_per_document=8,
    )

    # 3. Append to grounded_prompts.jsonl in the harness schema.
    appended = 0
    with OUT.open("a", encoding="utf-8") as f:
        for g in goldens[:25]:   # hard cap to keep the 25/25 split
            row = {
                "id": f"grounded-S{appended + 1:02d}",
                "surface": "chatbot",
                "prompt": g.input,
                "context_cards": [],   # substrate is already implicit in g.context
                "pinned_labels": [],
                "reference": g.expected_output,
                "disclaimer_required": True,
                "meta": {
                    "source": "optitrade_native",
                    "methodology": "deepeval_synth_v1",
                    "license": "internal",
                    "version": "2026-06-24",
                    "created_by": "deepeval-synth",
                    "source_id": None,
                    "provenance": (
                        "Auto-generated by DeepEval Synthesizer over a "
                        "representative PortfolioSnapshotResponse JSON; "
                        "methodology borrowed from RAGEval (arXiv:2408.01262)."
                    ),
                },
            }
            f.write(json.dumps(row, ensure_ascii=False) + "\n")
            appended += 1

    print(f"Appended {appended} Synthesizer rows → {OUT}")


if __name__ == "__main__":
    main()